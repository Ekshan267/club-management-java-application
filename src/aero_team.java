    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        {
            int[] rows = juventus2.getSelectedRows();
            DefaultTableModel model = (DefaultTableModel) juventus2.getModel();

            while(rows.length>0)
            {
                ((DefaultTableModel)model).removeRow(juventus2.convertRowIndexToModel(rows[0]));

                rows = juventus2.getSelectedRows();
            }
            juventus2.clearSelection();
        }       // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

